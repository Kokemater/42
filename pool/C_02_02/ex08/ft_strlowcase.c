char	*ft_strlowcase(char *str)
{
	int	i;
	int	dist;

	dist = 97 - 65;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
		{
			str[i] = str[i] + dist;
		}
		i++;
	}
	return (str);
}

int	ft_str_is_printable(char *str)
{
	int	is_impr;
	int	i;

	is_impr = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (! (str[i] >= 32 && str[i] <= 126))
		{
			is_impr = 0;
		}
		i++;
	}
	return (is_impr);
}
